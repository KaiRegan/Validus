      <h2>Some Interesting Facts:</h2>
        <p class="lead">Ben Nevis was shaped by fire and ice!
        Although today Ben Nevis is a pretty wet and wild part of the Scottish Highlands, this towering mountain was once sculpted by the forces of ice and fire. The mountain is all that remains of the inner dome of an enormous volcano that exploded around 350 million years ago.</p>
        <p class="lead">Clocking in at 1345m above sea level, Ben Nevis is Scotland’s highest mountain, and the tallest peak in the UK.
        In fact, the closest peak of a similar height is 700km across the North Sea in Norway!</p>
        <h2>Over 150,000 walkers make the ascent of Ben Nevis every year</h2>
        <p class=lead>
        Ben Nevis is one of the most heavily trafficked mountains in Scotland, partly because of its imposing stature and iconic status. It’s also a popular choice for fit, amateur hikers, as you don’t need any mountaineering skill to make the ascent. It usually takes around 4 hours to reach the summit, and a further two or three hours to descend.</p>
        <h2>It’s possible to see Northern Ireland from the summit</h2>
        <p class="lead">
        This fact about Ben Nevis might surprise you! Did you know that that the views from the top of Ben Nevis are so extraordinary that on a clear day you can see all the way to Northern Ireland?</p>
