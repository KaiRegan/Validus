                <h1>Instructions</h1>
                <p> There are nine exercises. These exercises are to be done in pairs and one triplet
                to save time. Pairing two exercises means doing a set of the first exercise, resting 90 seconds,
                then doing a set of the second exercise, resting 90 seconds, and repeating until you've done 3
                sets of that pair. For the triplet, you do a set of the first exercise, rest 60 seconds, do a set of the
                second exercise, rest 60 seconds, do a set of the third exercise, rest 60 seconds, and repeating
                until you've done 3 sets of that triplet.</p>
                    <hr>
                <h1>Rest Time</h1>
                <p>If 90 seconds is not enough, you can rest up to 3 minutes if you like.</p>
                <hr>
                <h1>Reps</h1>
                <p>​Overall, if your pull-up max is say 8 reps, then go for one rep short of failure, such as
                7-7-7 instead of 8-6-5. The key is to not work to failure for the first set or two because it
                significantly detracts from performance in later sets which reduces your ability to perform good
                reps and maximize volume to get stronger and bigger muscles. The 90 second pairing is so that
                there is minimum 3 minutes rest between each set, which is 3 minutes total. This rest time is
                where there is 99% replenishment of ATP in the muscles, so you are not limited by fatigue in the
                muscles. Sticking with failure-1 repetitions and 3 total minutes between sets of the same
                exercise allows you to maximize volume and effort level which is essential for strength and
                hypertrophy.</p>
                <hr>
                <h1>Order</h1>
                <p>You can switch the order of the exercises, as long as you leave the warm-up in the warm-up, and the strength work in the strength work.</p>
                <hr>
                <h1>Frequency</h1>
                <p>​This routine is written for 3x a week workouts, with at least one rest day in between
                workouts. For example you could do Mon/Weds/Fri, or Tues/Thurs/Sun, whatever works for your
                schedule. Don’t train every day - it does not make you more effective. Don't purposely split the
                workout into separate days. It's meant to be a full body workout.</p>
                <h1>Skill work</h1>
                <p> Want to go upside down? The skill work from the former RR has been moved to a
                ‘Skill Day’ routine, which can be slotted into this routine (before starting the strength work
                section) or practiced on rest days.</p>
